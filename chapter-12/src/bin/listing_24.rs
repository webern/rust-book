use std::error::Error;
use std::{env, fs, process};

/// Listing 24: prints errors to stderr instead of stdout. Boring but important for this type of
/// program because the user needs to differentiate between output and error messages.
fn main() {
    let args: Vec<String> = env::args().collect();

    let config = Config::build(&args).unwrap_or_else(|err| {
        eprintln!("Problem parsing arguments: {err}");
        process::exit(1);
    });

    // Now that the `run` function returns a `Result` we need to handle it here.
    if let Err(e) = run(config) {
        eprintln!("Application error: {e}");
        process::exit(1);
    }
}

